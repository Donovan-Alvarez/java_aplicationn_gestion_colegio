    private void actualizarTabla(){
        DefaultTableModel data = new DefaultTableModel(new String[]{"ID", "Nombre", "Seccion", "Fecha inicio", "Fecha fin", "Hora inicio", "Hora fin", "Profesor", "Nota"}, estudianteSeleccionado.cursos.size());
        jTable1.setModel(data);

        int row = 0;

        for(int i = 0; i < estudianteSeleccionado.cursos.size(); i++){
            Curso c = estudianteSeleccionado.cursos.get(i);
            jTable1.setValueAt(c.id, row, 0);
            jTable1.setValueAt(c.nombre, row, 1);
            jTable1.setValueAt(c.seccion, row, 2);
            jTable1.setValueAt(c.fechaInicio, row, 3);
            jTable1.setValueAt(c.fechaFin, row, 4);
            jTable1.setValueAt(c.horaInicio, row, 5);
            jTable1.setValueAt(c.horaFin, row, 6);
            jTable1.setValueAt(c.profesor, row, 7);
            jTable1.setValueAt(estudianteSeleccionado.notaFinal.get(i), row, 8);
            row++;
        }
    }
    
