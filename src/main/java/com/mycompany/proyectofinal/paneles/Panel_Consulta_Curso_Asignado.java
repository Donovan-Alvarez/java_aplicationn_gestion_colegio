    private void DesasignarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DesasignarActionPerformed
        int filaSeleccionada = jTable1.getSelectedRow();
        if (filaSeleccionada != -1) {
            String idCurso = jTable1.getValueAt(filaSeleccionada, 0).toString();
            for (int i = 0; i < estudianteSeleccionado.cursos.size(); i++) {
                if (estudianteSeleccionado.cursos.get(i).id.equals(idCurso)) {
                    estudianteSeleccionado.cursos.remove(i);
                    ((DefaultTableModel) jTable1.getModel()).removeRow(filaSeleccionada);
                    JOptionPane.showMessageDialog(this, "Curso desasignado");
                    return;
                }
            }
            JOptionPane.showMessageDialog(this, "No se pudo encontrar el curso seleccionado en la lista de cursos del estudiante");
        } else {
            JOptionPane.showMessageDialog(this, "No se ha seleccionado ninguna fila");
        }
    }//GEN-LAST:event_DesasignarActionPerformed

