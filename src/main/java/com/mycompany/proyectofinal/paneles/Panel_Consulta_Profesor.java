    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String contenido = new String();
        for(Profesor p : ProyectoFinal.profesors){

            contenido+= p.usuario + ",";
            contenido+= p.nombre + ",";
            contenido+= p.apellido + ",";
            contenido+= p.password + ",";
            contenido+= p.especialidad + "\n";
        }
        
        JFileChooser archivo = new JFileChooser();
        int retorno = archivo.showDialog(this,"Cargar");
        if(retorno==JFileChooser.APPROVE_OPTION){
            escribirArchivoTexto(archivo.getSelectedFile().toPath().toString(),contenido);
        }
             
    }//GEN-LAST:event_jButton5ActionPerformed

    public void escribirArchivoTexto(String rutaConNombre,String contenido){
    
        try{
            FileWriter fw = new FileWriter(rutaConNombre);
            PrintWriter pw = new PrintWriter(fw);
            pw.write(contenido);
            pw.close();
            fw.close();
        }
        catch(IOException e){
            e.printStackTrace();
        }
        
    }
    
    
    public void leerArchivoProfesor(String rutaNombre){
        try{
            File f = new File(rutaNombre);
            FileReader fr = new FileReader(f);
            
            BufferedReader br = new BufferedReader(fr);
            
            String linea; 
            while((linea = br.readLine()) != null){
                String[] datos = linea.split(",");
                Profesor p = new Profesor();
                p.usuario =  datos[0];
                p.nombre = datos[1];
                p.apellido = datos[2];
                p.password = datos[3];
                p.especialidad  = datos[4];
                        
                ProyectoFinal.profesors.add(p);
            }
            
            actualizarTabla();
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "No se pudo cargar el archivo", "", JOptionPane.ERROR_MESSAGE);
        }
    }
    
