    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Profesor profesor = new Profesor();
        profesor.usuario = jTextField1.getText();
        profesor.nombre = jTextField2.getText();
        profesor.apellido = jTextField3.getText();
        profesor.especialidad = jComboBox1.getSelectedItem().toString();
        
        System.out.println(validarContraseña(jTextField4.getText()));
        
        if (validarContraseña(jTextField4.getText())){
            profesor.password = jTextField4.getText();
            ProyectoFinal.profesors.add(profesor);

            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
        }else{
            JOptionPane.showMessageDialog(this, "Contraseña invalida", "", JOptionPane.WARNING_MESSAGE);
            jTextField4.setText("");
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

