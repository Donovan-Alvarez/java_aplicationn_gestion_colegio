    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        
        for (Curso c : ProyectoFinal.cursos){
            if (c.nombre.equals(jComboBox1.getSelectedItem().toString())){
                cursoSeleccionado = c;
                break;
            }
        }
        
        if(cursoSeleccionado != null){
            DefaultTableModel data = new DefaultTableModel(new String[]{"Carn√©", "Nombre"}, cursoSeleccionado.estudiantes.size());
            jTable1.setModel(data);
            
            int row = 0;
            
            for(Estudiante e : cursoSeleccionado.estudiantes){
                jTable1.setValueAt(e.carne, row, 0);
                jTable1.setValueAt(e.nombre, row, 1);
                row++;
            }
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

