package com.mycompany.proyectofinal.models;

import java.io.Serializable;
import java.util.ArrayList;

/**
 *
 * @author donovanpc
 */
public class Curso implements Serializable{
    private static final long serialVersionUid = 1L;
    public String id;
    public String nombre;
    public String seccion;
    public String fechaInicio;
    public String fechaFin;
    public String horaInicio;
    public String horaFin;
    public String profesor;
   
    public ArrayList<Estudiante> estudiantes = new ArrayList<>();

    public String getId() {
        return id;
    }

    public Curso() {
    }

    public Curso(String id, String nombre, String seccion, String fechaInicio, String fechaFin, String horaInicio, String horaFin, String profesor) {
        this.id = id;
        this.nombre = nombre;
        this.seccion = seccion;
        this.fechaInicio = fechaInicio;
        this.fechaFin = fechaFin;
        this.horaInicio = horaInicio;
        this.horaFin = horaFin;
        this.profesor = profesor;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getSeccion() {
        return seccion;
    }

    public void setSeccion(String seccion) {
        this.seccion = seccion;
    }

    public String getFechaInicio() {
        return fechaInicio;
    }

    public void setFechaInicio(String fechaInicio) {
        this.fechaInicio = fechaInicio;
    }

    public String getFechaFin() {
        return fechaFin;
    }

    public void setFechaFin(String fechaFin) {
        this.fechaFin = fechaFin;
    }

    public String getHoraInicio() {
        return horaInicio;
    }

    public void setHoraInicio(String horaInicio) {
        this.horaInicio = horaInicio;
    }

    public String getHoraFin() {
        return horaFin;
    }

    public void setHoraFin(String horaFin) {
        this.horaFin = horaFin;
    }

    public String getProfesor() {
        return profesor;
    }

    public void setProfesor(String profesor) {
        this.profesor = profesor;
    }

    public ArrayList<Estudiante> getEstudiantes() {
        return estudiantes;
    }

    public void setEstudiantes(ArrayList<Estudiante> estudiantes) {
        this.estudiantes = estudiantes;
    }

    
    
    
}
